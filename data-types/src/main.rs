fn main() {
    let age: i32 = 30;
    let score: u8 = 255;
    let temperature: i64 = -100000;

    println!("age: {}", age);
    println!("score: {}", score);
    println!("temperature: {}", temperature);
}
// i32
// i stands for signed integer, it can be positive or negative
// 32 means its 32 bits(4 bytes) in size

// i64
// i = signed
// 64 = 64bits (8bytes)

// u8
// u stands for unsigned integer,can only be zero or positive
// 8 means its 8bits (1byte)
// range from 0 to 255
